#include <string.h>
#include <stdio.h>
#include "main.h"

/**
 * _strcat - concatenates two strings
 * @dest: final result of the concatenated strings
 * @src: parameter to be concatenated
 * Return: a pointer to the concatenated char string
 */

char *_strcat(char *dest, char *src)
{
	strcat(dest, src);
}
